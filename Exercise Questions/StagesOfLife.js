var age = 24;
if (age < 2) {
    console.log("Baby");
}
else if (age >= 2 && age < 4) {
    console.log("Toddler");
}
else if (age >= 13 && age < 20) {
    console.log("Person is a teenager");
}
else if (age >= 20 && age < 65) {
    console.log("Person is an adult");
}
else {
    console.log("Person is an elder.");
}
